function myResNav() {
let x = document.getElementById("navlist2");
  if (x.style.display === "inline-block") {
    x.style.display = "none";
  } else {
    x.style.display = "inline-block";
  }
}
function closeMenu() {
  let exit= document.getElementById("navlist2");
  if (exit.style.display === "inline-block"){
    exit.style.display = "none";
  }
  }


  